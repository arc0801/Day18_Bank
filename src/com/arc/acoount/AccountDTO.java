package com.arc.acoount;

public class AccountDTO {

	private String id;
	private String aNum;
	private String aName;
	private int aBal;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getaNum() {
		return aNum;
	}
	public void setaNum(String aNum) {
		this.aNum = aNum;
	}
	public String getaName() {
		return aName;
	}
	public void setaName(String aName) {
		this.aName = aName;
	}
	public int getaBal() {
		return aBal;
	}
	public void setaBal(int aBal) {
		this.aBal = aBal;
	}
	
	
}
